/**
 *  Operadores logicos
 
    && -> AND -> E (todas as expressões devem ser verdadeiras).
    || -> OR -> OU (uma das expressões precisa ser verdadeira).
    ! -> NOT -> NÂO
 */

console.log(true && true && true);// retorna true
console.log(true && false && true);//retorna false

console.log(false || true);// retorna true
console.log(false || false);// retorna false
